public class Interval {
    public static void main(String[] args) {
        int a = 50;
        if (a >= 0 && a <= 14) {

            System.out.println("0-14 число а попадает от 0 до 14");
        }
        else if (a >= 15 && a <=35){
            System.out.println("15-35");
        }
        else if (a>=36&&a<=50){
            System.out.println("36-50");
        }
        else if (a>=50&&a<=100){
            System.out.println("50-100");
        }
    }
}
